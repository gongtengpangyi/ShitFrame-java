package shit.ioc.exception;


public class ShitIocConfigException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1437192925842909114L;

	public ShitIocConfigException() {
		super();
	}

	public ShitIocConfigException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ShitIocConfigException(String message, Throwable cause) {
		super(message, cause);
	}

	public ShitIocConfigException(String message) {
		super(message);
	}

	public ShitIocConfigException(Throwable cause) {
		super(cause);
	}

	
	
}
